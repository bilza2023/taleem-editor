<div class="container">
  <h1>Local Editing vs Hosted Playback</h1>

  <p>
    Taleem separates editing from playback.
  </p>

  <p>
    During editing, you may use local image and audio files.
  </p>

  <p>
    During playback, assets can be resolved from a hosted location.
  </p>

  <h2>Editing Mode</h2>

  <p>
    When working locally, you can reference assets
    stored on your machine.
  </p>

  <p>
    This allows fast development and testing.
  </p>

  <h2>Playback Mode</h2>

  <p>
    When presenting, the player resolves assets
    using the configured base URL.
  </p>

  <p>
    This makes it possible to host your images
    and audio files online.
  </p>

  <h2>Why This Separation Exists</h2>

  <p>
    Editing and presentation serve different purposes.
  </p>

  <p>
    Editing prioritizes flexibility and iteration.
  </p>

  <p>
    Playback prioritizes stability and reliability.
  </p>

  <h2>The Result</h2>

  <p>
    You can develop locally,
    host assets remotely,
    and present consistently.
  </p>

  <p>
    The presentation structure remains unchanged.
  </p>

</div>
